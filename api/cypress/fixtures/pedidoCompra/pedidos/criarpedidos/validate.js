export const  validatePedidos =  (response) => {
    expect(response).to.have.property('id').that.is.a('string');
    expect(response).to.have.property('codigo').that.is.a('number');
    expect(response).to.have.property('data').that.is.a('string');
    expect(response).to.have.property('dataEntrega').that.is.a('string');
    expect(response).to.have.property('fornecedor').that.is.an('object');
    expect(response.fornecedor).to.have.property('id').that.is.a('string');
    expect(response.fornecedor).to.have.property('descricao').that.is.a('string');
    expect(response.fornecedor).to.have.property('documentoPrincipal').that.is.a('string');
    expect(response).to.have.property('safra').that.is.an('object');
    expect(response.safra).to.have.property('id').that.is.a('string');
    expect(response.safra).to.have.property('descricao').that.is.a('string');
    expect(response).to.have.property('fazenda').that.is.an('object');
    expect(response.fazenda).to.have.property('id').that.is.a('string');
    expect(response.fazenda).to.have.property('descricao').that.is.a('string');
    expect(response).to.have.property('mesmoLocal').that.is.a('boolean');
    expect(response).to.have.property('fazendaEntrega').that.is.an('object');
    expect(response.fazendaEntrega).to.have.property('id').that.is.a('string');
    expect(response.fazendaEntrega).to.have.property('descricao').that.is.a('string');
    expect(response).to.have.property('empresa').that.is.an('object');
    expect(response.empresa).to.have.property('id').that.is.a('string');
    expect(response.empresa).to.have.property('descricao').that.is.a('string');
    expect(response.empresa).to.have.property('inscricaoEstadual').that.is.an('object');
    expect(response.empresa.inscricaoEstadual).to.have.property('id').that.is.a('string');
    expect(response.empresa.inscricaoEstadual).to.have.property('descricao').that.is.a('string');
    expect(response.empresa.inscricaoEstadual).to.have.property('valor').that.is.a('string');
    expect(response.empresa.inscricaoEstadual).to.have.property('isento').that.is.a('boolean');
    expect(response.empresa.inscricaoEstadual).to.have.property('ativo').that.is.a('boolean');
    expect(response).to.have.property('statusPedido').that.is.a('number');
    expect(response).to.have.property('pedidoMateriais').that.is.an('array');
    response.pedidoMateriais.forEach((material) => {
        expect(material).to.have.property('id').that.is.a('string');
        expect(material).to.have.property('quantidade').that.is.a('number');
        expect(material).to.have.property('saldo').that.is.a('number');
        expect(material).to.have.property('valorUnitario').that.is.a('number');
        expect(material).to.have.property('valorTotal').that.is.a('number');
        expect(material).to.have.property('material').that.is.an('object');
        expect(material.material).to.have.property('id').that.is.a('string');
        expect(material.material).to.have.property('descricao').that.is.a('string');
        expect(material).to.have.property('unidadeMedida').that.is.an('object');
        expect(material.unidadeMedida).to.have.property('id').that.is.a('string');
        expect(material.unidadeMedida).to.have.property('descricao').that.is.a('string');
    });
    expect(response).to.have.property('pedidoPagamento').that.is.an('object');
    expect(response.pedidoPagamento).to.have.property('id').that.is.a('string');
    expect(response.pedidoPagamento).to.have.property('quantidadeParcelas').that.is.a('number');
    expect(response.pedidoPagamento).to.have.property('valorFixo').that.is.a('boolean');
    expect(response.pedidoPagamento).to.have.property('formaPagamento').that.is.an('object');
    expect(response.pedidoPagamento.formaPagamento).to.have.property('id').that.is.a('string');
    expect(response.pedidoPagamento.formaPagamento).to.have.property('descricao').that.is.a('string');
    expect(response.pedidoPagamento).to.have.property('pedidoPagamentoParcelas').that.is.an('array');
    response.pedidoPagamento.pedidoPagamentoParcelas.forEach((parcela) => {
        expect(parcela).to.have.property('id').that.is.a('string');
        expect(parcela).to.have.property('numeroParcela').that.is.a('number');
        expect(parcela).to.have.property('dataVencimento').that.is.a('string');
        expect(parcela).to.have.property('valorParcela').that.is.a('number');
    })
}